///////////////////////////////////////////////
//		INCLUDES
///////////////////////////////////////////////

#include <iostream>
#include <fstream>
#include <string>

#include <cctype>

using namespace std;

///////////////////////////////////////////////
//		MAIN
///////////////////////////////////////////////

int main ( int argc, char ** argv ) {

	///////////////////////////////////////////////
	//		ARG CHECK
	///////////////////////////////////////////////

	if (argc != 2) {
		cerr << "\nUsage: " << argv[0] << " <fileName>\n\n";
		exit(1);
	}

	///////////////////////////////////////////////
	//		VARS
	///////////////////////////////////////////////

	string input = "";

	int i = 0;
	
	bool paren = false;

	///////////////////////////////////////////////
	//		OPEN FILES
	///////////////////////////////////////////////

	ifstream in;
	in.open(argv[1]);

	ofstream out;
	out.open(string(string(argv[1])+".txt").c_str());

	//////////////////////////////////////////////////////
	//		READ BY LINE
	//////////////////////////////////////////////////////

	while (!in.eof()) {

		getline(in, input);
		
		if (input.length() != 0) {

			paren = false;
			for (i=1; i<input.length(); i++) {
				if (input[i] == '(') {
					paren = true;
					break;
				}			
			}

			if (!paren) {
				i = 0;
				while (!isspace(input[i]))
					i++;
	
				//////////////////////////////////////////////////////
				//		PRINT TO FILE
				//////////////////////////////////////////////////////
	
				out << input.substr(0,i) << "\n";
			}

		}

	}

	///////////////////////////////////////////////
	//		CLOSE FILES
	///////////////////////////////////////////////

	in.close();

	out.close();

	///////////////////////////////////////////////
	//		END
	///////////////////////////////////////////////

	return 0;

}